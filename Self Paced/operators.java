public class operators {

    public static void main(String[] args) {
        // Pre- increment  pahle value increase kro phir store karo
        int a = 10;
        int b = ++a; // increase then use
        System.out.println(a);
        System.out.println(b);

        // Post - increment assign the value then increase
        int x = 10;
        int y = x++;
        System.out.println(x);
        System.out.println(y);
    }
}
